function eurMapTooltips()
    x,y=M2TWEOP.getGameTileCoordsWithCursor();
    xPos, yPos = ImGui.GetMousePos();
    if xPos > half_window_pos_right then
        xPos = xPos-200
    else
        xPos = xPos+50
    end
    if ruin_tiles[tostring(x..y)] then
        if ruin_tiles[tostring(x..y)].active then
            ImGui.SetNextWindowPos(xPos, yPos)
            open, shouldDraw = ImGui.Begin("Tile Information", open, ImGuiWindowFlags.NoDecoration)
                ImGui.BeginTooltip()
                ImGui.Text("Village of "..ruin_tiles[tostring(x..y)].message);
                ImGui.EndTooltip()
            ImGui.End()
        end
    end
end
